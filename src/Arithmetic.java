public class Arithmetic {
    public static void main(String[] args) {
        int a=7;
        int b=42;
        System.out.println(42+7);
        System.out.println(42-7);
        System.out.println(7-42);
        System.out.println(7*42);
        System.out.println(7/42);
        System.out.println(42/7);
        System.out.println(42%7);
        System.out.println(7%42);
        System.out.println(a--);
        System.out.println(b--);
        System.out.println(a++);
        System.out.println(b++);
    }
}
